package com.colliderlabs;

public class ColliderException extends Exception {

	public ColliderException() {
		super();
	}

	public ColliderException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ColliderException(String arg0) {
		super(arg0);
	}

	public ColliderException(Throwable arg0) {
		super(arg0);
	}

}
